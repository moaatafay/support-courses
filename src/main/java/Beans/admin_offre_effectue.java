package Beans;

public class admin_offre_effectue {
	private String matiere;
	private String prof_name;
	private String prof_family_name;
	private String student_name;
	private String student_family_name;
	private String date;
	private int rate;
	private String lebelle;
	public String getMatiere() {
		return matiere;
	}
	public void setMatiere(String matiere) {
		this.matiere = matiere;
	}
	public String getProf_name() {
		return prof_name;
	}
	public void setProf_name(String prof_name) {
		this.prof_name = prof_name;
	}
	public String getProf_family_name() {
		return prof_family_name;
	}
	public void setProf_family_name(String prof_family_name) {
		this.prof_family_name = prof_family_name;
	}
	public String getStudent_name() {
		return student_name;
	}
	public void setStudent_name(String student_name) {
		this.student_name = student_name;
	}
	public String getStudent_family_name() {
		return student_family_name;
	}
	public void setStudent_family_name(String student_family_name) {
		this.student_family_name = student_family_name;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getRate() {
		return rate;
	}
	public void setRate(int rate) {
		this.rate = rate;
	}
	public String getLebelle() {
		return lebelle;
	}
	public void setLebelle(String lebelle) {
		this.lebelle = lebelle;
	}
}
