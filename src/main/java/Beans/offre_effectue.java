package Beans;

public class offre_effectue {
	int id_offre_effect;
	String student;
	String profname;
	String date;
	String status;
	int rate_offre_efect;
	String nom_offre;
	public String getProfname() {
		return profname;
	}
	public void setProfname(String profname) {
		this.profname = profname;
	}
	public int getRate_offre_efect() {
		return rate_offre_efect;
	}
	public void setRate_offre_efect(int rate_offre_efect) {
		this.rate_offre_efect = rate_offre_efect;
	}
	public String getNom_offre() {
		return nom_offre;
	}
	public void setNom_offre(String nom_offre) {
		this.nom_offre = nom_offre;
	}
	public int getId_offre_effect() {
		return id_offre_effect;
	}
	public void setId_offre_effect(int id_offre_effect) {
		this.id_offre_effect = id_offre_effect;
	}
	public String getStudent() {
		return student;
	}
	public void setStudent(String student) {
		this.student = student;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
}
